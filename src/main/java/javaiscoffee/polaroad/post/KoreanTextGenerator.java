    private static final Random random = new Random();

    public String generateKoreanText(int maxLength) {
        StringBuilder result = new StringBuilder();
        while (result.length() < maxLength) {
            String word = KOREAN_STRGINS[random.nextInt(KOREAN_STRGINS.length)];
            if (result.length() + word.length() > maxLength) break;
            result.append(word).append(" ");
        }
        return result.toString().trim();
    }

    public List<String> generateHashtags(int startNum, int maxLength) {
        ArrayList<String> tags = new ArrayList<>();
        for(int i = 0; i < maxLength; i++) {
            tags.add(KOREAN_WORDS[startNum + i]);
        }
        return tags;
    }
